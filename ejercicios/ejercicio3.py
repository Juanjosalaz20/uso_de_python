'''
Problema: Escribe un programa que solicite un número al usuario y lo incremente en 5 unidades y 
luego lo decremente en 3.
'''
N1 = float(input('Ingrese un número: '))

Incremento = N1 + 5
print('Este es tu número mas 5 unidades: ', Incremento)

Resta = Incremento - 3

print('Este es tu número mas 5 unidades menos 3 unidades: ', Resta)